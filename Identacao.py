'''A indentação é uma característica importante no Python, pois além de promover a legibilidade,
é essencial para o bom funcionamento do código. Enquanto na maioria das linguagens, como C, Java
e PHP, os blocos de código são delimitados por chaves ({ }) ou comandos, em Python, os blocos são
delimitados por espaços ou tabulações, formando uma indentação visual. Não existem símbolos de “abre” e “fecha”.
Exemplo abaixo'''

